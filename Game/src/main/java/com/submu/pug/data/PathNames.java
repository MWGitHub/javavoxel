public final class PathNames {
    /**
     * Config file path.
     */
    public static final String CONFIG_FILE = "config";

    /**
     * Program's assets path.
     * The program installer should make the program directory writable.
     */
    public static final String ROOT_ASSETS_DIRECTORY = "assetsDirectory";

    /**
     * User's path where downloaded content and temporary content is stored.
     */
    public static final String USER_DIRECTORY = "userDirectory";

    /**
     * Core directory for the assets.
     */
    public static final String CORE_ASSETS_DIRECTORY = "core";

    /**
     * Temporary directory for transitional files.
     */
    public static final String TEMP_DIRECTORY = "temp";

    /**
     * Directory for unpacked map contents.
     */
    public static final String MAP_DIRECTORY = "map";
}
